'''
1. Pedir al usuario que intruduzca dos numeros.
2. hacer operaciones basicas de la calculadora (sum, rest, mul, div)

'''

num_uno = int(input('Ingresa tu primer numero: '))
num_dos = int(input('Ingresa tu segundo numero: '))

print(f"la suma es = {num_uno + num_dos}")
print(f"la resta es = {num_uno - num_dos}")
print(f"la multiplicacion es = {num_uno * num_dos}")
print(f"la division es = {num_uno / num_dos}")
